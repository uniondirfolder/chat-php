<script>
    let receiver_id = '';
    let my_id = "{{ \Illuminate\Support\Facades\Auth::id() }}";
    $(document).ready(function () {
        // ajax setup form csrf token
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });

        const pusher = new Pusher('ae9344bdef72cf7e7fe1', {
            cluster: 'eu',
        });

        let channel = pusher.subscribe('nvv-chanel');

        channel.bind('nvv-event', function (data) {

            if (my_id === data.from) {
                $('#' + data.to).click();
            } else if (my_id === data.to) {
                if (receiver_id === data.from) {
                    // if receiver is selected, reload the selected user ...
                    $('#' + data.from).click();
                } else {

                    let pending = parseInt($('#' + data.from).find('.pending').html());

                    if (pending) {
                        $('#' + data.from).find('.pending').html(pending + 1);
                    } else {
                        $('#' + data.from).append('<span class="pending">1</span>');
                    }
                }
            }
        });

        $('.user').click(function () {
            $('.user').removeClass('active');
            $(this).addClass('active');
            $(this).find('.pending').remove();

            receiver_id = $(this).attr('id');

            $.ajax({
                type: "get",
                url: "message/" + receiver_id,
                data: "",
                cache: false,
                success: function (data) {
                    $('#messages').html(data);
                    scrollToBottomFunc();
                }
            });
        });

        $(document).on('keyup', '.input-text input', function (e) {
            let message = $(this).val();
            if (e.keyCode === 13 && message !== '' && receiver_id !== '') {
                $(this).val('');

                let datastr = "receiver_id=" + receiver_id + "&message=" + message;
                $.ajax({
                    type: "post",
                    url: "message",
                    data: datastr,
                    cache: false,
                    success: function (data) {

                    },
                    error: function (jqXHR, status, err) {
                    },
                    complete: function () {
                        $.ajax({
                            type: "get",
                            url: "message/" + receiver_id,
                            data: "",
                            cache: false,
                            success: function (data) {
                                $('#messages').html(data);
                                scrollToBottomFunc();
                            }
                        });
                    }
                })
            }
        });
    });

    function scrollToBottomFunc() {
        $('.message-wrapper').animate({
            scrollTop: $('.message-wrapper').get(0).scrollHeight
        }, 50);
    }

</script>

